// Write a function that takes the base and height of a triangle and return its area.

function areaOfTriangle(base, height) {
    return `${(base * height) / 2} square meter`
}

// inputs
console.log(areaOfTriangle(1, 2)) // 1 square meter
console.log(areaOfTriangle(12, 8)) // 48 square meter
console.log(areaOfTriangle(3, 9)) // 13.5 square meter